n1 = int(input("Escreva um número inteiro e positivo\n->"))
n2 = int(input("Escreva outro número inteiro e positivo\n->"))
n3 = int(input("Escreva mais um número inteiro e positivo\n->"))
n4 = int(input("Escreva outro número inteiro e positivo\n->"))

media = (n1 + n2 + n3 + n4) / 4

print("A média dos números {} {} {} e {} é igual a: {}".format(n1, n2, n3, n4, media))